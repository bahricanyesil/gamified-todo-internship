/// Enum to determine the size types.
enum Sizes {
  /// Refers to the extreme low valued sizes.
  extremeLow,

  /// Refers to the low valued sizes.
  low,

  /// Refers to the low-medium valued sizes.
  lowMed,

  /// Refers to the medium valued sizes.
  med,

  /// Refers to the medium-high valued sizes.
  medHigh,

  /// Refers to the high valued sizes.
  high,
}
